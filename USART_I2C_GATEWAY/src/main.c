#include "stm32f10x.h"
#include "I2C_lib.h"
#include "USART_lib.h"

/* Private typedef */
/* Private define  */
const char StartChar = '[';
const char EndChar = 	']';

/* Private macro */
/* Private variables */
uint16_t recievedData;		/**< variable stores USART received bytes count */
uint8_t convDone = 0;		/**< status variable of conversion from ascii to hex */
uint8_t readData = 0;		/**< variable stores status if master should read from slave */
uint16_t readDataCount = 0;	/**< variable stores number of bytes to be read from slave */
uint8_t base = 16;			/**< stores base of hexadecimal system for conversion */
uint8_t pass;				/**< variable stores number of passes when converting hex value from ascii */

/* Private function prototypes */
void flush_i2cData();
void getDataCount();
/* Private functions */

